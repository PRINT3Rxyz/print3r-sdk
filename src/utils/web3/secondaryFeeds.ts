export const checkForSecondaryFeed = (
  chainId: number,
  customId: string
): FeedResult => {
  if (customId.endsWith(":1")) {
    const ticker = customId.split(":")[0];

    // Check in chainlinkPriceFeeds
    for (const feeds of chainlinkPriceFeeds) {
      if (feeds[chainId] && feeds[chainId][ticker]) {
        return { type: "chainlink", feed: feeds[chainId][ticker] };
      }
    }

    // Check in pythFeeds
    if (pythFeeds[ticker]) {
      return { type: "pyth", feed: pythFeeds[ticker] };
    }
  }

  // If neither exists
  return { type: false, feed: "" };
};

export const getChainlinkFeed = (chainId: number, ticker: string) => {
  for (const feeds of chainlinkPriceFeeds) {
    if (feeds[chainId] && feeds[chainId][ticker]) {
      return feeds[chainId][ticker];
    }
  }
};

// Check if chain supports any chainlink feeds at all
export const chainHasChainlinkFeed = (chainId: number) => {
  for (const feeds of chainlinkPriceFeeds) {
    if (feeds[chainId]) {
      return true;
    }
  }
  return false;
};

export const getPythFeed = (ticker: string): string | undefined => {
  return pythFeeds[ticker];
};
